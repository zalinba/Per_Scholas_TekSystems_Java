public class Slide31_BooleanLiterals {
    public static void main(String[] args) {
        //Only two values are allowed for boolean literals: true and false.
        boolean t = true;
        boolean f = false;
        System.out.println("t is equal to: " +t);
        System.out.println("f is equal to: " +f);

    }
}
