/* An integer literal can be assigned to an integer variable,
 * provided it is within the range of the variable’s type.
 *
 * An integer literal is assumed to be an int, whose value is
 * between -231 (-2,147,483,648) to 231–1 (2,147,483,647).
 *
 * To denote a long literal, append it with the letter “L.”
 * Lowercase “l” is allowed, but is strongly discouraged because
 * it is easily be confused with “1” (the digit).
 */
public class Slide25_Integer_int_Literals {
    public static void main(String[] args) {
        int i = 312;
        byte b = 58;	// valid, since 58 < 128.
        long lval = 3885L;  //Letter L put at the end of the long data type variable value


    }

}
