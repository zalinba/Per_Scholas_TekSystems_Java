public class Slide27_CharLiterals {
    public static void main(String[] args) {
        /* Character literals are enclosed in single quotation marks.
         * Any printable character, except for the backslash (\), can
         * be specified this way.
         */
        char a = 'A', nine = '9', plus = '+', tilde = '~';


        /* Alternatively, we can specify a char literal as an integer
         * literal. This code is identical to the code above:
         */
        char b = 0101, number = 57, add = 0x2b, sqiggly = 126;


        /* The integer can be specified using either decimal, octal,or
         * hexadecimal forms. The allowed range is 0 to 65535.
         */


    }
}
