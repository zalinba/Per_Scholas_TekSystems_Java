public class Slide26_FloatingPointLiterals {
    public static void main(String[] args) {
    /* Floating-point literals use a period character (.) for the decimal point.
     * The following statement uses a literal to initialize a double variable:
     */
        double length = 1.5;

    /* All floating-point literals are considered type double.
     * To specify a literal of float, append the letter F
     * (F or f) to the literal:
     */
        float pi = 3.1415927F;

    /* Floating point literals can be written using scientific notation.
     * The ‘e’ may be either lower- or upper-case.
     */
        double avogadrosNumber = 6.022e23;
        double plancksConstant = 6.626e-34;

    }
}
