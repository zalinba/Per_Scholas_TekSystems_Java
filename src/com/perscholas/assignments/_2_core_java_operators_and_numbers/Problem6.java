//Lab 303.1.3 Practice Assignment Operators and Numbers

/*                      Problem 6
 * Write a program that declares an integer variable, assigns a number,
 * and uses a postfix increment operator to increase the value. Print
 * the value before and after the increment operator.
 */
public class Problem6 {
    public static void main(String[] args) {
        int m = 3;

        System.out.println("the value of m before postfix increment is "+m);

        m++;

        System.out.println("the value of m after postfix increment is "+m);

    }
}
