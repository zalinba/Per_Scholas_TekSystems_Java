/*              Problem 7
 * Write a program that declares a named constant
 * and uses it in a calculation. Print the result.
 */
public class Problem7 {
    public static void main(String[] args) {
        final int A = 1;
        final int B = 2;
        final int SUM = A + B;

        System.out.println("Problem 7: The sum of A + B is equal to " + SUM);
        /* Output:
        Problem 7: The sum of A + B is equal to 3
         */

    }
}
