package com.perscholas.lab._3_string_methods;
//Lab 303.2.1

public class charAt {
    /* The charAt() method grabs a character
     * from a String at specified length it
     * is located
     */
    public static void main(String[] args) {
        String message = "Welcome to Java";
        System.out.println("The first character in the message is " + message.charAt(0));
        //output:The first character in the message is W
    }
}
